package it.unibo.cicciopier.controller.menu;

public enum MenuPanel {
    /**
     * The main menu UI showed at the start of the game
     */
    MAIN_MENU,
    /**
     * The Level selection menu UI used to choose which level to play
     */
    LEVEL_SELECTION,
    /**
     * The Leaderboard UI of a specific level
     */
    LEADERBOARD,
    /**
     * The settings menu UI used to change game settings
     */
    SETTINGS,
}
